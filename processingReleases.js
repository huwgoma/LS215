// Write a function named processReleaseData that processes the following movie data:
// - The function should return a new array of new objects containing only the id and 
//    title properties.
// - Keep only releases where ID and title are both present.
// - Assume that id > 0 and title is non-empty
// - Keep only the id and title data for each release.
// - Do not mutate the original objects.


function processReleaseData(data) {
  return data
    .filter(movie => movie.id && movie.title)
    .map(movie => { return { id: movie.id, title: movie.title } });
}

